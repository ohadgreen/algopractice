package questions.model;

public class SecondValue {
    private float second;
    private int value;

    public SecondValue(float second, int value) {
        this.second = second;
        this.value = value;
    }
    public float getSecond() {
        return second;
    }

    public void setSecond(float second) {
        this.second = second;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
